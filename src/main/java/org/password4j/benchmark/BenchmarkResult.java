package org.password4j.benchmark;

public class BenchmarkResult
{

    private long rounds;

    private long timing;


    public long getRounds()
    {
        return rounds;
    }

    public void setRounds(long rounds)
    {
        this.rounds = rounds;
    }

    public long getTiming()
    {
        return timing;
    }

    public void setTiming(long timing)
    {
        this.timing = timing;
    }
}
